<section>
    <h2>Objectifs de la Politique de Recyclages</h2>
    <div class="vertical-box">
        <h3>Optimiser le Tri et le Recyclage</h3>
        <ul>
            <li>ÉcoGestUM permet de trier et recycler efficacement les équipements en fin de vie (meubles, matériel
                informatique, livres, etc.).</li>
            <li>Chaque objet recyclé est tracé pour garantir une gestion transparente et responsable.</li>
            <li>Objectif : Réduire les déchets envoyés en décharge de 30 % d'ici 2027.</li>
        </ul>
    </div>
</section>


